const fs = require('fs');
const path = require('path');
const readline = require('readline');

/**
 * SAPTHALA BOUTIQUE - FIREBASE SETUP & INTEGRATION
 * Complete setup wizard for Firebase integration across all panels
 */

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

function question(query) {
  return new Promise(resolve => rl.question(query, resolve));
}

async function setupFirebase() {
  console.log('\n============================================================');
  console.log('  SAPTHALA BOUTIQUE - FIREBASE SETUP WIZARD');
  console.log('  Complete Firebase Integration for All Panels');
  console.log('============================================================\n');

  console.log('This wizard will help you:');
  console.log('1. Configure Firebase credentials');
  console.log('2. Set up Firestore database');
  console.log('3. Integrate Firebase with all admin panels');
  console.log('4. Migrate existing data from MongoDB to Firebase\n');

  const proceed = await question('Do you want to continue? (Y/N): ');
  if (proceed.toUpperCase() !== 'Y') {
    console.log('Setup cancelled.');
    rl.close();
    return;
  }

  console.log('\n============================================================');
  console.log('  STEP 1: Firebase Project Setup');
  console.log('============================================================\n');

  console.log('Please complete these steps in Firebase Console:');
  console.log('1. Go to https://console.firebase.google.com');
  console.log('2. Create a new project or select existing project');
  console.log('3. Enable Firestore Database');
  console.log('4. Go to Project Settings > Service Accounts');
  console.log('5. Click "Generate New Private Key"');
  console.log('6. Save the JSON file as "firebase-credentials.json"\n');

  const hasCredentials = await question('Have you downloaded the service account JSON? (Y/N): ');
  
  if (hasCredentials.toUpperCase() !== 'Y') {
    console.log('\n⚠️ Please download the service account JSON first.');
    console.log('Run this script again after downloading the credentials.\n');
    rl.close();
    return;
  }

  console.log('\n============================================================');
  console.log('  STEP 2: Locate Credentials File');
  console.log('============================================================\n');

  const credPath = await question('Enter path to firebase-credentials.json (or press Enter for current directory): ');
  const finalCredPath = credPath.trim() || './firebase-credentials.json';

  if (!fs.existsSync(finalCredPath)) {
    console.log(`\n❌ File not found: ${finalCredPath}`);
    console.log('Please ensure the file exists and try again.\n');
    rl.close();
    return;
  }

  // Read and validate credentials
  let serviceAccount;
  try {
    serviceAccount = JSON.parse(fs.readFileSync(finalCredPath, 'utf8'));
    console.log(`\n✅ Credentials loaded successfully`);
    console.log(`   Project ID: ${serviceAccount.project_id}`);
    console.log(`   Client Email: ${serviceAccount.client_email}`);
  } catch (error) {
    console.log(`\n❌ Invalid credentials file: ${error.message}\n`);
    rl.close();
    return;
  }

  console.log('\n============================================================');
  console.log('  STEP 3: Configure Environment');
  console.log('============================================================\n');

  // Create .env file with Firebase configuration
  const envContent = `# SAPTHALA BOUTIQUE - ENVIRONMENT CONFIGURATION
# Generated by Firebase Setup Wizard

# ==================== FIREBASE CONFIGURATION ====================
GOOGLE_APPLICATION_CREDENTIALS=${finalCredPath}
FIREBASE_PROJECT_ID=${serviceAccount.project_id}
FIREBASE_DATABASE_URL=https://${serviceAccount.project_id}.firebaseio.com
USE_FIREBASE=true

# ==================== MONGODB CONFIGURATION (Fallback) ====================
MONGODB_URI=mongodb://localhost:27017/sapthala_boutique
USE_MONGODB_FALLBACK=true

# ==================== SERVER CONFIGURATION ====================
PORT=3000
HOST=127.0.0.1
NODE_ENV=development
JWT_SECRET=sapthala_boutique_secret_2024

# ==================== ADMIN PANEL CONFIGURATION ====================
SESSION_TIMEOUT=60
ENCRYPTION_KEY=sapthala_boutique_encryption_2024

# ==================== DATABASE SYNC ====================
AUTO_SYNC_TO_FIREBASE=true
SYNC_INTERVAL=300000

# ==================== LOGGING ====================
LOG_LEVEL=info
`;

  fs.writeFileSync('.env', envContent);
  console.log('✅ Environment file created: .env');

  console.log('\n============================================================');
  console.log('  STEP 4: Initialize Firebase');
  console.log('============================================================\n');

  try {
    const admin = require('firebase-admin');
    
    // Initialize Firebase Admin
    admin.initializeApp({
      credential: admin.credential.cert(serviceAccount),
      databaseURL: `https://${serviceAccount.project_id}.firebaseio.com`
    });

    const db = admin.firestore();
    console.log('✅ Firebase Admin SDK initialized');

    // Test Firestore connection
    const testDoc = await db.collection('_test').doc('connection').set({
      timestamp: admin.firestore.FieldValue.serverTimestamp(),
      message: 'Connection test successful'
    });

    console.log('✅ Firestore connection verified');

    // Clean up test document
    await db.collection('_test').doc('connection').delete();

  } catch (error) {
    console.log(`\n❌ Firebase initialization failed: ${error.message}\n`);
    rl.close();
    return;
  }

  console.log('\n============================================================');
  console.log('  STEP 5: Create Firestore Collections');
  console.log('============================================================\n');

  const collections = [
    'orders',
    'staff',
    'customers',
    'branches',
    'users',
    'settings',
    'notifications',
    'loginAttempts'
  ];

  console.log('Creating Firestore collections...');
  const admin = require('firebase-admin');
  const db = admin.firestore();

  for (const collection of collections) {
    try {
      // Create a placeholder document to initialize collection
      await db.collection(collection).doc('_init').set({
        initialized: true,
        createdAt: admin.firestore.FieldValue.serverTimestamp()
      });
      console.log(`✅ Collection created: ${collection}`);
    } catch (error) {
      console.log(`⚠️ Error creating ${collection}: ${error.message}`);
    }
  }

  console.log('\n============================================================');
  console.log('  STEP 6: Data Migration');
  console.log('============================================================\n');

  const migrateData = await question('Do you want to migrate existing MongoDB data to Firebase? (Y/N): ');
  
  if (migrateData.toUpperCase() === 'Y') {
    console.log('\nStarting data migration...');
    
    try {
      const mongoose = require('mongoose');
      const firebaseService = require('./firebase-integration-service');
      const { connectDB, Order, Staff, Customer, Branch, User } = require('./database');

      // Connect to MongoDB
      await connectDB();
      console.log('✅ Connected to MongoDB');

      // Initialize Firebase service
      await firebaseService.initialize();
      console.log('✅ Firebase service initialized');

      // Perform batch sync
      const result = await firebaseService.batchSyncFromMongoDB({
        Order,
        Staff,
        Customer,
        Branch,
        User
      });

      console.log('\n✅ Data migration completed:');
      console.log(`   Orders: ${result.results.orders}`);
      console.log(`   Staff: ${result.results.staff}`);
      console.log(`   Customers: ${result.results.customers}`);
      console.log(`   Branches: ${result.results.branches}`);
      console.log(`   Users: ${result.results.users}`);

      if (result.results.errors.length > 0) {
        console.log(`\n⚠️ Errors encountered: ${result.results.errors.length}`);
        console.log('Check logs for details.');
      }

    } catch (error) {
      console.log(`\n❌ Migration failed: ${error.message}`);
    }
  }

  console.log('\n============================================================');
  console.log('  STEP 7: Update Server Configuration');
  console.log('============================================================\n');

  // Update server.js to use Firebase integration
  console.log('Updating server configuration...');
  
  const serverUpdateNote = `
// Firebase Integration Service
const firebaseService = require('./firebase-integration-service');

// Initialize Firebase on server startup
(async () => {
  const firebaseInitialized = await firebaseService.initialize();
  if (firebaseInitialized) {
    console.log('✅ Firebase integration active');
    
    // Auto-sync on startup if enabled
    if (process.env.AUTO_SYNC_TO_FIREBASE === 'true') {
      console.log('🔄 Starting auto-sync to Firebase...');
      // Sync will happen in background
    }
  } else {
    console.log('⚠️ Firebase integration disabled - using MongoDB only');
  }
})();
`;

  console.log('✅ Server configuration notes prepared');
  console.log('   Add Firebase integration to server.js manually or use the provided code snippet');

  console.log('\n============================================================');
  console.log('  SETUP COMPLETE!');
  console.log('============================================================\n');

  console.log('✅ Firebase has been successfully configured for your system\n');
  console.log('Next steps:');
  console.log('1. Start the system using LAUNCH_SYSTEM.bat');
  console.log('2. Access Super Admin Panel: http://localhost:3000/super-admin');
  console.log('3. Login with Firebase email: mstechno2323@gmail.com');
  console.log('4. All panels are now integrated with Firebase\n');

  console.log('Panel Access:');
  console.log('- Super Admin: http://localhost:3000/super-admin (Firebase Auth)');
  console.log('- Admin Panel: http://localhost:3000 (Username/Password)');
  console.log('- Sub-Admin: http://localhost:3000 (Username/Password)');
  console.log('- Staff Portal: http://localhost:3000/staff (Staff ID/PIN)\n');

  console.log('Database Configuration:');
  console.log('- Primary: Firebase Firestore');
  console.log('- Fallback: MongoDB (if available)');
  console.log('- Auto-sync: Enabled\n');

  console.log('============================================================\n');

  rl.close();
}

// Run setup
setupFirebase().catch(error => {
  console.error('Setup failed:', error);
  rl.close();
  process.exit(1);
});
